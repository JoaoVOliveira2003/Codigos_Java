public class EstruturasBasicasWhileEFor {
    public static void main(String[] args){
        int i=1,j;

        System.out.print("Em While ");
        while(i<10){
            System.out.print(i+" ");
            i++;
        }
        
        System.out.println("Em for");
        for(j=1;j<10;j++){
            System.out.print(j+" ");
        }
    }
}
